"""E2E live LLM checks for classify gate inside agent()."""

import pytest

from src.agent import agent

pytestmark = [pytest.mark.llm, pytest.mark.e2e]

IRRELEVANT_MESSAGE = 'Вопрос не релевантен для агента'


@pytest.mark.parametrize(
    'query',
    [
        'Расскажи анекдот про кота',
        'Какая погода завтра в Москве?',
        'Напиши письмо работодателю на английском',
        'Сколько будет 123 * 456?',
        'Придумай название для кофейни',
        'Сделай краткое резюме романа "Война и мир"',
        'Напиши SQL-запрос для выборки пользователей за месяц',
        'Объясни, как работает asyncio в Python',
        'Составь план поездки в Стамбул на 3 дня',
        'Какие акции технологических компаний сейчас интересны?',
        'Подбери рецепт пасты с грибами и сливками',
        'Придумай 10 названий для pet-проекта',
        'Как улучшить качество сна без лекарств?',
        'Переведи текст на испанский язык',
        'Напиши мотивационное письмо в магистратуру',
        'Сгенерируй поздравление с днем рождения',
        'Посоветуй фильм на вечер в жанре триллер',
        'Как быстро выучить английские времена?',
        'Создай контент-план для блога на месяц',
        'Сделай SWOT-анализ кофейни в центре города',
        'Как починить ошибку ModuleNotFoundError в Python?',
        'Сравни iPhone и Android по плюсам и минусам',
        'Придумай сценарий для reels про фитнес',
        'Составь рацион питания на 2000 ккал',
        'Напиши регулярное выражение для email',
    ],
)
def test_agent_e2e_irrelevant_returns_fixed_message(query: str):
    """Irrelevant queries must be rejected with fixed message."""
    result = agent(query)
    assert str(result.get('answer') or '') == IRRELEVANT_MESSAGE
