"""E2E checks for vector_search lector-oriented cases through agent()."""

import pytest

from src.agent import agent

pytestmark = [pytest.mark.agent]


@pytest.mark.parametrize(
    ('query', 'expected_answer'),
    [
        ('лектор по мл', 'Соколов Евгений Андреевич'),
        ('лектор по машинному обучению', 'Соколов Евгений Андреевич'),
        ('лектор по машинке', 'Соколов Евгений Андреевич'),
        ('лектор по теории вероятностей', 'Семаков Сергей Львович'),
        ('лектор по теорверу', 'Семаков Сергей Львович'),
        ('лектор по вероятностям', 'Семаков Сергей Львович'),
        ('лектор по теории оптимизации', 'Кропотов Дмитрий Александрович'),
        ('лектор по оптимизации', 'Кропотов Дмитрий Александрович'),
        ('лектор по метоптам', 'Кропотов Дмитрий Александрович'),
    ],
)
def test_agent_e2e_vector_search_simple_lector(query: str, expected_answer: str):
    result = agent(query)
    assert str(result.get('answer') or '') == expected_answer


@pytest.mark.parametrize(
    ('query', 'expected_answer'),
    [
        ('кто лектор по дисциплине ml', 'Соколов Евгений Андреевич'),
        ('лектором по мл является', 'Соколов Евгений Андреевич'),
        ('машинка читает лекции кто', 'Соколов Евгений Андреевич'),
        ('кто лектор по курсу probability theory', 'Семаков Сергей Львович'),
        ('по вероятности ведет лекции кто', 'Семаков Сергей Львович'),
        ('по теорверу лектор это', 'Семаков Сергей Львович'),
        ('кто ведет лекции по оптам', 'Кропотов Дмитрий Александрович'),
        ('кто по части лекций оптимизации', 'Кропотов Дмитрий Александрович'),
        (
            'кто отвечает за лекционную часть оптимизации',
            'Кропотов Дмитрий Александрович',
        ),
    ],
)
def test_agent_e2e_vector_search_complex_lector(query: str, expected_answer: str):
    result = agent(query)
    assert str(result.get('answer') or '') == expected_answer
